#!/usr/bin/env python3
"""
PyVault - Python source obfuscator/deobfuscator.

This tool obfuscates Python source files into a lambda-based loader with random-looking
byte strings, and can decrypt files generated by this tool back into the original source.

Author: https://github.com/davizofficial
"""

import os
import random
import re
import string
import zlib
from pathlib import Path

APP_NAME = "PyVault"
AUTHOR = "https://github.com/davizofficial"
HEADER_PREFIX = "# PyVault"
HEADER = f"{HEADER_PREFIX} - Coded by {AUTHOR}"
VERSION = "1.0.0"

# Lime green terminal color
LIME = "\033[38;2;50;255;50m"
DIM = "\033[2m"
RED = "\033[91m"
YELLOW = "\033[93m"
CYAN = "\033[96m"
RESET = "\033[0m"


def clear_screen():
    os.system("cls" if os.name == "nt" else "clear")


def pause():
    input(f"\n{DIM}Press Enter to continue...{RESET}")


def color(text: str, code: str = LIME) -> str:
    return f"{code}{text}{RESET}"


def random_name(length=18):
    chars = string.ascii_letters + "_"
    return "_" + "".join(random.choice(chars) for _ in range(length))


def bytes_to_escape(data: bytes) -> str:
    return "".join("\\x%02x" % b for b in data)


def escape_to_bytes(text: str) -> bytes:
    return bytes(int(x, 16) for x in re.findall(r"\\x([0-9a-fA-F]{2})", text))


def xor_bytes(data: bytes, key: bytes) -> bytes:
    return bytes(data[i] ^ key[i % len(key)] for i in range(len(data)))


def rol8(value: int, shift: int) -> int:
    shift %= 8
    return ((value << shift) & 0xFF) | (value >> (8 - shift))


def ror8(value: int, shift: int) -> int:
    shift %= 8
    return (value >> shift) | ((value << (8 - shift)) & 0xFF)


def obfuscate_bytes(source: bytes):
    compressed = zlib.compress(source, level=9)

    key1 = os.urandom(random.randint(24, 64))
    key2 = os.urandom(random.randint(24, 64))
    shift = random.randint(1, 7)

    layer = xor_bytes(compressed, key1)
    layer = bytes(rol8(b, shift) for b in layer)
    layer = xor_bytes(layer, key2)

    return layer, key1, key2, shift


def deobfuscate_bytes(payload: bytes, key1: bytes, key2: bytes, shift: int):
    layer = xor_bytes(payload, key2)
    layer = bytes(ror8(b, shift) for b in layer)
    layer = xor_bytes(layer, key1)
    return zlib.decompress(layer)


def build_loader(payload: bytes, key1: bytes, key2: bytes, shift: int) -> str:
    n_payload = random_name()
    n_key1 = random_name()
    n_key2 = random_name()
    n_shift = random_name()
    n_zlib = random_name()
    n_i = random_name()

    payload_s = bytes_to_escape(payload)
    key1_s = bytes_to_escape(key1)
    key2_s = bytes_to_escape(key2)

    expr = (
        f"(((({n_payload}[{n_i}]^{n_key2}[{n_i}%len({n_key2})])>>{n_shift})|"
        f"((({n_payload}[{n_i}]^{n_key2}[{n_i}%len({n_key2})])<<(8-{n_shift}))&255))"
        f"^{n_key1}[{n_i}%len({n_key1})])"
    )

    return (
        f"{HEADER_PREFIX}:{shift} | {HEADER} | Version {VERSION}\n"
        f"(lambda {n_payload},{n_key1},{n_key2},{n_shift},{n_zlib}:"
        f"exec(compile("
        f"{n_zlib}.decompress(bytes([{expr} for {n_i} in range(len({n_payload}))])),"
        f"__file__,'exec')))"
        f'(b"{payload_s}",b"{key1_s}",b"{key2_s}",{shift},__import__(\'zlib\'))\n'
    )


def extract_parts(text: str):
    if HEADER_PREFIX not in text:
        raise ValueError("This file does not contain a valid PyVault header.")

    byte_strings = re.findall(r'b"((?:\\x[0-9a-fA-F]{2})+)"', text)
    if len(byte_strings) < 3:
        raise ValueError("Payload or keys were not found. Invalid PyVault file format.")

    payload = escape_to_bytes(byte_strings[0])
    key1 = escape_to_bytes(byte_strings[1])
    key2 = escape_to_bytes(byte_strings[2])

    match = re.search(r"# PyVault:(\d+)", text)
    if not match:
        raise ValueError("Shift value was not found in the PyVault header.")

    shift = int(match.group(1))
    if shift < 1 or shift > 7:
        raise ValueError("Invalid shift value.")

    return payload, key1, key2, shift


