def ask_path(prompt: str) -> str:
    return input(color(prompt, CYAN)).strip().strip('"').strip("'")


def menu_obfuscate():
    clear_screen()
    show_banner()
    print(color("\n[ Obfuscate Python (Encrypt) ]\n", YELLOW))

    input_file = ask_path("Input Python file: ")
    if not input_file:
        print(color("Input cannot be empty.", RED))
        pause()
        return

    input_path = Path(input_file)
    if not input_path.exists():
        print(color(f"File not found: {input_path}", RED))
        pause()
        return

    if input_path.suffix.lower() != ".py":
        print(color("Warning: the input file does not use the .py extension.", YELLOW))

    default_output = input_path.with_name(input_path.stem + "_enc.py")
    output_file = ask_path(f"Output file [{default_output}]: ")
    output_path = Path(output_file) if output_file else default_output

    try:
        source = input_path.read_bytes()
        payload, key1, key2, shift = obfuscate_bytes(source)
        loader = build_loader(payload, key1, key2, shift)
        output_path.write_text(loader, encoding="utf-8")

        print(color("\n[OK] File successfully obfuscated.", LIME))
        print(color(f"Input : {input_path}", CYAN))
        print(color(f"Output: {output_path}", CYAN))
        print(color("\nRun the obfuscated file with:", YELLOW))
        print(color(f"python3 {output_path}", CYAN))
    except Exception as error:
        print(color(f"\n[ERROR] Obfuscation failed: {error}", RED))

    pause()


def menu_deobfuscate():
    clear_screen()
    show_banner()
    print(color("\n[ Deobfuscate Python (Decrypt) ]\n", YELLOW))

    input_file = ask_path("Input obfuscated file: ")
    if not input_file:
        print(color("Input cannot be empty.", RED))
        pause()
        return

    input_path = Path(input_file)
    if not input_path.exists():
        print(color(f"File not found: {input_path}", RED))
        pause()
        return

    stem = input_path.stem
    if stem.endswith("_enc"):
        stem = stem[:-4]
    default_output = input_path.with_name(stem + "_dec.py")

    output_file = ask_path(f"Output file [{default_output}]: ")
    output_path = Path(output_file) if output_file else default_output

    try:
        text = input_path.read_text(encoding="utf-8", errors="replace")
        payload, key1, key2, shift = extract_parts(text)
        source = deobfuscate_bytes(payload, key1, key2, shift)
        output_path.write_bytes(source)

        print(color("\n[OK] File successfully deobfuscated.", LIME))
        print(color(f"Input : {input_path}", CYAN))
        print(color(f"Output: {output_path}", CYAN))
    except Exception as error:
        print(color(f"\n[ERROR] Deobfuscation failed: {error}", RED))

    pause()


def main_menu():
    while True:
        clear_screen()
        show_banner()
        choice = (
            input(color("Choose an option [1/2] or q to quit: ", CYAN)).strip().lower()
        )

        if choice == "1":
            menu_obfuscate()
        elif choice == "2":
            menu_deobfuscate()
        elif choice in {"q", "quit", "exit"}:
            print(color("Goodbye.", LIME))
            break
        else:
            print(color("Invalid option.", RED))
            pause()


if __name__ == "__main__":
    main_menu()
